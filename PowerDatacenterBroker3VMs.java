public class PowerDatacenterBroker3VMs extends DatacenterBroker {

	/**
	 * Instantiates a new PowerDatacenterBroker.
	 * 
	 * @param name the name of the broker
	 * @throws Exception the exception
	 */
	public PowerDatacenterBroker3VMs(String name) throws Exception {
		super(name);
	}

	/**
	 * Create the submitted virtual machines in a datacenter.
	 *
	 * @param datacenterId Id of the chosen Datacenter
	 * @pre $none
	 * @post $none
	 * @see #submitVmList(java.util.List)
	 */
	@Override
	protected void createVmsInDatacenter(int datacenterId) {
		// send as much vms as possible for this datacenter before trying the next one
		int requestedVms = 0;
		String datacenterName = CloudSim.getEntityName(datacenterId);
		for (Vm vm : getVmList()) {
			if(requestedVms == 3)
				break;
			if (!getVmsToDatacentersMap().containsKey(vm.getId())) {
				Log.printLine(CloudSim.clock() + ": " + getName() + ": Trying to Create VM #" + vm.getId()
						+ " in " + datacenterName);
				sendNow(datacenterId, CloudSimTags.VM_CREATE_ACK, vm);
				requestedVms++;
			}
		}

		getDatacenterRequestedIdsList().add(datacenterId);

		setVmsRequested(requestedVms);
		setVmsAcks(0);
	}
}
